def build_grid() -> Grid:
    return Grid(GRID_ROWS, LEGEND, AREAS, street="長街")


def build_world(run_id: str, seed: int) -> WorldState:
    world = WorldState(run_id=run_id, scenario=NAME, seed=seed, tick=0)
    for spec in AGENTS:
        world.agents[spec["id"]] = AgentState(
            id=spec["id"],
            name=spec["name"],
            role=spec["role"],
            persona=spec["persona"],
            home_area=spec["home_area"],
            pos=Pos(*spec["start"]),
            skill=spec["skill"],
        )
    return world
